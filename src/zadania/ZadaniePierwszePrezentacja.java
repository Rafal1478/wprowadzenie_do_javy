package zadania;

import java.util.Scanner;
public class ZadaniePierwszePrezentacja {
    public static void main(String[] args) {
        for(int i=0;i<5;i++){
            System.out.println("Aktualna wartość i:"+i);
        }System.out.println("");
        for(int i=0;i<=5;i++){
            System.out.println("Aktualna wartość i:"+i);
        }System.out.println("");
        for(int i=0;i<=5;++i){
            System.out.println("Aktualna wartość i:"+i);
        }
    }
}
